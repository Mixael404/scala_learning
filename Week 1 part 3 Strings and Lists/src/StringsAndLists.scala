object StringsAndLists {
  def main(args: Array[String]): Unit = {
    var s1: List[String] = List("Mary", "had", "a", "little", "lamb")
    print(s1(0).head.toUpper + " ")
    print(s1(1).head.toUpper + " ")
    print(s1(2).head.toUpper + " ")
    print(s1(3).head.toUpper + " ")
    print(s1(4).head.toUpper + " ")
  }
}
